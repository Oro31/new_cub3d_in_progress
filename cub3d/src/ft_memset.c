#include "cub3d.h"

void	ft_memset_spr_data(t_data *s, int n)
{
	int		i;

	i = -1;
	while (++i < n)
	{
		s[i].img = NULL;
		s[i].addr = NULL;
		s[i].bpp = 0;
		s[i].ll = 0;
		s[i].e = 0;
	}
}
